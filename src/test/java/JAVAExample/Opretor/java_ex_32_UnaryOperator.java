package JAVAExample.Opretor;


public class java_ex_32_UnaryOperator {
    public static void main(String[] args) {
        int a = 5;

        System.out.println(++a); // 6    a = 5+1 = 6
        System.out.println(a++); // 6    a = 6 then a+1  =7
        System.out.println(a);   // 7    a = 7

    }
}
