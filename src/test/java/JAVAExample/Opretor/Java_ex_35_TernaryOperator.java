package JAVAExample.Opretor;

public class Java_ex_35_TernaryOperator {
    public static void main(String[] args) {
      //  Shortcut of if-else
        //condition ? true_value : false_value;

        int a = 30;
        int b = 20;

        int min = (a < b) ? a : b;
        System.out.println("Lower number = " + min);

    }


}
