package JAVAExample.Opretor;


public class java_ex_29_relationalOretor {
    public static void main(String[] args) {
        // Relational operators
        int abhi_age = 34;
        int bara_age = 30 ;
        boolean result = bara_age > abhi_age ;
        boolean result2 = bara_age < abhi_age;
        boolean result3 = bara_age <= abhi_age;
        boolean result4 = bara_age == abhi_age;


        System.out.println(result);
        System.out.println(result2);
        System.out.println(result3);
        System.out.println(result4);

    }
}
