package JAVAExample.Opretor;

public class Java_ex_34_TernaryOperator {
    public static void main(String[] args) {
      //  Shortcut of if-else
        //condition ? true_value : false_value;

        int a = 10;
        int b = 20;

        int max = (a > b) ? a : b;
        System.out.println("Greater number = " + max);
    }


}
