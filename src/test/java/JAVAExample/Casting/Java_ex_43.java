package JAVAExample.Casting;

public class Java_ex_43 {
    public static void main(String[] args) {

//        Multiple widening
         byte x= 5;
         short y = x;
         int z = y;
        System.out.println(z);
         long l = z;
        System.out.println(l);
         float f = l;
        System.out.println(f);
         double d = f;
        System.out.println(d);

    }
}
