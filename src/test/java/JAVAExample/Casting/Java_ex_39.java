package JAVAExample.Casting;

public class Java_ex_39 {
    public static void main(String[] args) {

        // Implicit casting
        int x = 100;
        double y = x;
        System.out.println("Implicit: " + y);

        // Explicit casting
        double a = 25.75;
        int b = (int) a;
        System.out.println("Explicit: " + b);
    }
}
